    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        System.out.println("asu");
        Setting setting = new Setting();
        String setPass;
        if(setPassword.isSelected()){
            setPass = "YES";
        }
        else{
            setPass = "NO";
        }
        
        String setPerkiraan;
        if(noPerkiraan.isSelected()){
            setPerkiraan = "NO";
        }
        else if(mingguPerkiraan.isSelected()){
            setPerkiraan = "Minggu";
        }
        else {
            setPerkiraan = "Bulan";
        }
        String pass;
        if(setPass == "YES"){
            if(password.getText().equals(rePassword.getText())){
                pass = password.getText();

            }else{
                showMessageDialog(null, "password dan re-type password tidak sama");
                pass = "";
            }
        }
        else{
            pass = "";
        }
            error=setting.submit(setPass, pass, setPerkiraan);  
            this.setVisible(false);
        
        
    }//GEN-LAST:event_btnSubmitActionPerformed

