    public void setIsi(String set_password, String password, String perkiraan){
        
        if("YES".equals(set_password)){
            setPassword.setSelected(true);
        }
        else if("NO".equals(set_password)){
            noSetPassword.setSelected(true);
        }
        
        if("Minggu".equals(perkiraan)){
            mingguPerkiraan.setSelected(true);
        }
        else if("Bulan".equals(perkiraan)){
            bulanPerkiraan.setSelected(true);
        }
        else if("NO".equals(perkiraan)){
            noPerkiraan.setSelected(true);
        }
        
    }
    
